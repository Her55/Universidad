    public void llenarComboBox() {
        ArrayList<Alumno> alumnos = new ArrayList<>();
        AlumnoData ad = new AlumnoData();
        alumnos = ad.listarAlumno();
        for (Alumno alumno : alumnos) {
            comboBoxAlumno.addItem(alumno);
        }
    }

    private void armarCabecera() {
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("A√±o");
        jtMaterias.setModel(modelo);
    }

    private void llenarTablaCursadas() {
        ArrayList<Materia> materiaCursadas = new ArrayList<>();
        // int sumador=0;
        InscripcionData id = new InscripcionData();
        materiaCursadas = id.ListarMateriasCursadas(idAlumno);
        for (Materia materia : materiaCursadas) {
            cargarDatos(materia);
        }
    }

    private void llenarTablaNoCursadas() {
        ArrayList<Materia> materiaNoCursadas = new ArrayList<>();
        // int sumador=0;
        InscripcionData id = new InscripcionData();
        materiaNoCursadas = id.ListarMateriasNOCursadas(idAlumno);
        for (Materia materia : materiaNoCursadas) {
            cargarDatos(materia);
        }
    }

    private void cargarDatos(Materia materia) {
        modelo.addRow(new Object[]{materia.getIdMateria(), materia.getNombre(), materia.getAnio()});
    }

    private void limpiarTabla() {
        for (int i = 0; i < jtMaterias.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
    }

